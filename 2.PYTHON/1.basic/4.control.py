count = 0

print('while')
#while: ~할때까지
while (count < 5):
    print(count)
    count = count + 1

while (count <= 10):
    print(count)
    count = count + 2

print('for')
for i in range(10):
    print(i)

users = ['park', 'kim', 'lee', 'son']
for i in users:
    print(i)

for num in [0,1,2,3,4,5]:
    print(num)

for num in [-5, 7, 133, 'park', 'sesac']:
    print(num)

for i in range(3,7): # range(3,7) = [3,4,5,6]
    print(i)

for i in range(1,10,2): # range(1,10,2) = [1,3,4,7,9]
    print(i)




